/**
  * @brief  Callback d'Adresse : Appelé quand le Maître (RPi) adresse le STM32.
  * @note   Cette fonction décide si la transaction sera une Écriture (PING) ou une Lecture (PONG).
  */
void HAL_I2C_AddrCallback(I2C_HandleTypeDef *hi2c, uint8_t TransferDirection, uint16_t AddrMatchCode)
{
  if (TransferDirection == I2C_DIRECTION_TRANSMIT)
  {
    // Master veut écrire (nous envoie PING)
    // On se prépare à recevoir PING_CMD_LEN (4) octets
    HAL_I2C_Slave_Seq_Receive_IT(hi2c, rxData, PING_CMD_LEN, I2C_FIRST_AND_LAST_FRAME);
  }
  else
  {
    // Master veut lire (attend PONG)
    // On envoie PONG_RSP_LEN (4) octets
    HAL_I2C_Slave_Seq_Transmit_IT(hi2c, txData, PONG_RSP_LEN, I2C_FIRST_AND_LAST_FRAME);
  }
}

